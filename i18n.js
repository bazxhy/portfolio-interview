// i18n Engine
// ============================================================
const I18n = {
    current: 'zh',

    init() {
        const saved = localStorage.getItem('portfolio-lang');
        if (saved && (saved === 'zh' || saved === 'en')) {
            this.current = saved;
        }
        this.apply();
        this.updateButtons();
    },

    toggle(lang) {
        if (lang === this.current) return;
        this.current = lang;
        localStorage.setItem('portfolio-lang', lang);
        this.apply();
        this.updateButtons();
    },

    apply() {
        const dict = I18N[this.current];
        if (!dict) return;

        // Set <html lang>
        document.documentElement.lang = this.current === 'zh' ? 'zh-CN' : 'en';

        // Update page title
        const titleMap = {
            zh: '关福博 — AI 驱动的新一代开发者',
            en: 'Guan Fubo — AI-Driven Next-Gen Developer',
        };
        document.title = titleMap[this.current] || titleMap.zh;

        // Apply all data-i18n elements
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.dataset.i18n;
            if (dict[key]) {
                // Buttons with text + icon — only replace the text node
                if (el.tagName === 'A' && el.classList.contains('btn')) {
                    const svg = el.querySelector('svg');
                    if (svg) {
                        // Remove all text nodes and replace
                        const texts = [];
                        el.childNodes.forEach(n => {
                            if (n.nodeType === 3) texts.push(n);
                            else if (n !== svg && n.nodeType === 1 && n.tagName !== 'svg') texts.push(n);
                        });
                        texts.forEach(n => {
                            if (n.nodeType === 3) n.textContent = dict[key];
                            else if (n.textContent) n.textContent = dict[key];
                        });
                        // Also set via innerText approach for clean replacement
                        const afterSvg = el.innerHTML.replace(/<svg[^>]*>[\s\S]*?<\/svg>/, '||SVG||');
                        const parts = afterSvg.split('||SVG||');
                        if (parts.length >= 2) {
                            el.innerHTML = parts[0].trim() + '||SVG||' + dict[key];
                            el.innerHTML = el.innerHTML.replace('||SVG||', svg.outerHTML);
                        }
                    }
                }
                // Handle <br> in hero.bio
                if (dict[key].includes('<br>')) {
                    el.innerHTML = dict[key];
                } else if (dict[key].includes('<strong>')) {
                    el.innerHTML = dict[key];
                } else {
                    // Keep child elements but replace text
                    const walkAndReplace = (node, text) => {
                        if (node.nodeType === 3) {
                            node.textContent = text;
                            return true;
                        }
                        if (node.children && node.children.length > 0) {
                            // Has child elements — try to find text nodes
                            let replaced = false;
                            node.childNodes.forEach(c => {
                                if (c.nodeType === 3 && !replaced) {
                                    c.textContent = text;
                                    replaced = true;
                                }
                            });
                            if (replaced) return true;
                        }
                        return false;
                    };
                    if (!walkAndReplace(el, dict[key])) {
                        el.textContent = dict[key];
                    }
                }
            }
        });
    },

    updateButtons() {
        document.querySelectorAll('.lang-btn').forEach(btn => {
            btn.classList.toggle('on', btn.dataset.lang === this.current);
        });
    },

    t(key) {
        return I18N[this.current]?.[key] || I18N.zh[key] || key;
    },
};

// Initialize on script load (before DOMContentLoaded so data is ready)
I18n.init();
